def sumar (valor1=0, valor2=0):
    return valor1 + valor2
def restar (valor1=0, valor2=0):
    return valor1 - valor2
def multiplicar (valor1=0, valor2=1):
    return valor1 * valor2
def dividir (valor1=0, valor2=1):
    return valor1 / valor2
